package javaCollection;

public class CollectionFramework {
	
	/*
	 * --------Java collection/ Collection Framework-----
	 * # use to store and processing dynamic multiple data.
	 * 
	 * 	1> List: dynamic array.
	 * 		a> ArrayList : processing slow(due to use of stack to manipulate data).
	 * 					   better to use for data storage.
	 * 		b> LinkedList : fast processing, better to use data processing.
	 * 
	 *  2> Set: collection of unique data.
	 *  	a> HashSet : unique data only.
	 *  	b> TreeSet : unique + sorted data.
	 *  	c> LinkedHashSet : unique + insertion in order.
	 *  
	 *  3> Map: data manage in key, value pair.
	 *  	a> HashMap : <k,v> pair.
	 *  	b> TreeMap : <k,v> + sorted data.
	 *  	c> LinkedHashMap : <k,v> + insertion in order.
	 */

}
