package abstraction;

public class AbstractionDocs {
	
	/*
	 * ----------Abstraction------------
	 * # process of hiding implementation details.
	 * # to achieve abstraction;
	 *   1> Abstract class:
	 *   	# class which contains abstract(unimplemented) methods and 
	 *        non-abstarct(implemented) methods is known as abstract class.
	 *   		
	 *   		#Abstract Method:
	 *   			* method which does not have body/implementation.
	 *   			e.g.
	 *   			 abstract void add();
	 *   			 abstract int getArea(int l, int b);
	 *      
	 *      # every abstract class must have at least one child class and child class must
	 *        override abstract methods.
	 *      
	 *      # we can't create object of abstract class.
	 *      # 0-100% abstraction.
	 *      
	 *      syntax:
	 *        	abstract class class_name{
	 *        		
	 *        		//abstract methods;
	 *        		//non-abstract methods;
	 *      }
	 *   
	 *   2> Interface:
	 *       
	 *       //act as an button to help interact tv and human(metaphor to understand)
	 *   
	 *   	# same as class which contains public abstract methods and
	 *   	  public static final variables.
	 *   	# we can't create object of interface
	 *   	# every interface must have at least one implementation class
	 *   	  and implementation class must override abstract methods.
	 *   	
	 *   	syntax:
	 *   	 interface interface_name{
	 *   		//public abstract methods
	 *   		//public static final variables
	 *   }
	 */

}
