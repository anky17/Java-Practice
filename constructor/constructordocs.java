package constructor;

public class constructordocs {
	
	/*
	 *                            --------Constructors--------
	 * # use to create object in java.
	 * # special type of method which has same name as class name is known as constructor.
	 * # constructor does not have any return type.
	 * # if there is no any user defined constructors in class, then java provides default constructor 
	 * to create object.
	 * 
	 * # TYPES:
	 * 	
	 * 		a) Default constructor: use to create object and set default values to it.
	 * 					
	 * 				Syntax:
	 * 						class_name(){
	 * 								//set default values to object
	 * 						}
	 * 
	 * 		b) Parameterized Constructor: use to create object and set dynamic values to it
	 * 			
	 * 				Syntax:
	 * 						class_name(arg1,arg2,...,argn){
	 * 								//set dynamic values to object
	 * 						}
	 * 
	 *   -> Constructor returns the object
	 */

}
